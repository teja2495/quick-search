    fun applyContactActionHint(
        updated: SearchUiState,
        hasSeenContactActionHint: Boolean,
    ): SearchUiState {
        val hasContacts = updated.contactResults.isNotEmpty() || updated.pinnedContacts.isNotEmpty()
        val shouldShowHint =
            hasContacts &&
                updated.hasContactPermission &&
                !updated.showContactActionHint &&
                !hasSeenContactActionHint

        return if (shouldShowHint) updated.copy(showContactActionHint = true) else updated
    }
